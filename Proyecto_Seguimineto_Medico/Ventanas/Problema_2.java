    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        // TODO add your handling code here:
        String SintomaX = campoSintoma.getText().trim();
        String Tratamiento = campoTratamiento.getText().trim();
        NodoH R = v1.getA().getLh().getP();
        while (R != null) {
            LD_NormalD ld = R.getH().getLd();
            NodoD S = ld.getP();
            while (S != null) {
                if (S.getD().getSintomas().equals(SintomaX)) {
                    if(Tratamiento.equals("")){
                       S.getD().setTratamiento("Fisioterapia");
                    }else{
                        S.getD().setTratamiento(Tratamiento);
                    }
                    System.out.println("modificacion");
                    System.out.println("id_medico: " + S.getD().getId_medico());
                    System.out.println("sintomas: " + S.getD().getSintomas());
                    System.out.println("tratamieno: " + S.getD().getTratamiento());
                }
                S = S.getSig();
            }
            R = R.getSig();
        }
    }//GEN-LAST:event_btnModificarActionPerformed

