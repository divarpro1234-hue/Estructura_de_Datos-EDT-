    private void btnMostrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMostrarActionPerformed
        // TODO add your handling code here:
        String titulos[] = {"ID medico", "Sintomas", "tratamiento"};
        DefaultTableModel pantalla = new DefaultTableModel(null, titulos);
        NodoH R = v1.getA().getLh().getP();
        while (R != null) {
            LD_NormalD ld = R.getH().getLd();
            NodoD S = ld.getP();
            while (S != null) {
                System.out.println("modificacion");
                System.out.println("id_medico: " + S.getD().getId_medico());
                System.out.println("sintomas: " + S.getD().getSintomas());
                System.out.println("tratamieno: " + S.getD().getTratamiento());
                String fila [] = {S.getD().getId_medico(),S.getD().getSintomas(),S.getD().getTratamiento()};
                pantalla.addRow(fila);
                System.out.println("---------------------------------");
                S = S.getSig();
            }
            R = R.getSig();
            Pantalla_interfaz.setModel(pantalla);
        }
    }//GEN-LAST:event_btnMostrarActionPerformed

