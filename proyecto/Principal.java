	private static void buscarEventoZMesXMostrar(LS_NormalMes a, String eventox, String mesx) {
		// TODO Auto-generated method stub
		NodoMes R = a.getP();
		int c = 0;
		while(R != null) {
			if(R.getMes().equalsIgnoreCase(mesx)) {
				
				LD_NormalCC lcc = R.getLcc();
				NodoCC S = lcc.getP();
				while(S != null) {
					LS_NormalE le = S.getCenCon().getLe();
					NodoE T = le.getP();
					System.out.println("centro de convencion: "+S.getCenCon().getNomCentro());
					while(T != null) {
						if(T.getEvento().getNombre().equalsIgnoreCase(eventox)) {
							c++;
							System.out.println("evento: "+T.getEvento().getNombre());
						}
						T = T.getSig();
					}
					S =S.getSig();
				}
				System.out.println("el evento '"+eventox+"' ocurrio "+c+" veces en el "+mesx);
			}
				
			R = R.getSig();
		}
	}
